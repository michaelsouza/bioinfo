function view_molecule(x)
plot3(x(:,1),x(:,2),x(:,3),...
    'MarkerFaceColor',[0 0 1],...
    'MarkerEdgeColor',[0 0 1],...
    'MarkerSize',15,...
    'Marker','o',...
    'LineWidth',5,...
    'Color',[0.501960813999176 0.501960813999176 0.501960813999176]);
xlabel('x');
ylabel('y');
zlabel('z');
title(sprintf('Molecule with %d atoms', length(x)));
box on
grid on
daspect([1 1 1])
end